int wait_count;

/********************************************
* プロトタイプ宣言
********************************************/

/********************************************
* エンド画面：初期化処理
* 引数：なし
* 戻り値：エラー情報【−1：異常有、−１以外：正常終了）
********************************************/
int EndScene_Initialize(void)
{
	int ret = 0;
	wait_count = 0;
	return ret;
}

/********************************************
* エンド画面：更新処理
* 引数：なし
* 戻り値：なし
********************************************/
void EndScene_Update(void)
{
	wait_count++;
}

/********************************************
* エンド画面：描画処理
* 引数：なし
* 戻り値：なし
********************************************/
void Get_EndTime(void)
{
	if (wait_count > 300)
	{
		return TRUE;
	}
	return FALSE;
}